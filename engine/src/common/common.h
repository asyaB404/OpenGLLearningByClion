#pragma once

// ============================================================================
// 窗口大小改变回调函数
// ============================================================================
// 当用户调整窗口大小时，GLFW 会自动调用这个函数
// 参数：
//   - window: 窗口指针
//   - width:  新的窗口宽度
//   - height: 新的窗口高度
void framebuffer_size_callback(GLFWwindow* window, int width, int height);

// ============================================================================
// 输入处理函数
// ============================================================================
// 每帧都会调用这个函数来检查用户的键盘输入
// 参数：
//   - window: 窗口指针
void processInput(GLFWwindow* window);

